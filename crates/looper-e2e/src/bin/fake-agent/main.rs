//! fake-agent binary — a fake AI coding agent used in E2E tests.
//!
//! Instead of calling a real LLM vendor, this binary performs simple
//! file-system actions controlled by environment variables.
//!
//! Environment variables:
//! - `LOOPER_E2E_FAKE_AGENT_MODE` — operation mode
//!   - `success-no-diff`: exit 0, stdout = completion marker
//!   - `success-with-diff`: exit 0, writes file specified by WRITE_FILE/MODIFY_FILE
//!   - `failure`: exit 1, stderr = transient error
//!   - `timeout`: sleep ignoring sleep_ms, simulates hang
//!   - `no-marker`: exit 0, no completion marker in stdout
//! - `LOOPER_E2E_FAKE_AGENT_ARTIFACT_DIR` — output directory for evidence
//! - `LOOPER_E2E_FAKE_AGENT_WRITE_FILE` — path to create
//! - `LOOPER_E2E_FAKE_AGENT_MODIFY_FILE` — path to modify (append)
//! - `LOOPER_E2E_FAKE_AGENT_SLEEP_MS` — milliseconds to sleep before responding
//! - `LOOPER_E2E_FAKE_AGENT_GIT_PATH` — path to git binary
//! - `LOOPER_E2E_FAKE_AGENT_GH_PATH` — path to gh binary
//! - `LOOPER_COMPLETION_MARKER` — marker string prefix for output
//! - `LOOPER_PROMPT` — the agent prompt

#[allow(unused_imports)]
use {
    anyhow as _, chrono as _, looper_agent as _, looper_config as _, looper_e2e as _, looper_git as _,
    looper_github as _, looper_runner as _, looper_scheduler as _, looper_service as _, looper_storage as _,
    looper_types as _, nix as _, regex as _, reqwest as _, serde as _, serde_json as _, tempfile as _, thiserror as _,
    tokio as _, uuid as _,
};

use std::io::Write;
use std::process;

const DEFAULT_MARKER: &str = "__LOOPER_RESULT__=";

fn main() {
    let mode = std::env::var("LOOPER_E2E_FAKE_AGENT_MODE").unwrap_or_else(|_| "success-no-diff".to_string());
    let artifact_dir = std::env::var("LOOPER_E2E_FAKE_AGENT_ARTIFACT_DIR").unwrap_or_else(|_| ".".to_string());
    let _ = std::fs::create_dir_all(&artifact_dir);

    // Write evidence file
    write_evidence(&artifact_dir, &mode);

    // Sleep if configured
    if let Ok(sleep_ms) = std::env::var("LOOPER_E2E_FAKE_AGENT_SLEEP_MS") {
        if let Ok(ms) = sleep_ms.parse::<u64>() {
            if ms > 0 && mode != "timeout" {
                std::thread::sleep(std::time::Duration::from_millis(ms));
            }
        }
    }

    match mode.as_str() {
        "timeout" => loop {
            std::thread::sleep(std::time::Duration::from_secs(3600));
        },
        "failure" => {
            eprintln!("fake-agent: agent process exited with transient error");
            process::exit(1);
        }
        "no-marker" => {
            println!("fake-agent: completed without marker");
            process::exit(0);
        }
        "success-with-diff" | "success-no-diff" => {
            if let Some(write_file) = try_get_env("LOOPER_E2E_FAKE_AGENT_WRITE_FILE") {
                let content = "// Auto-generated by fake-agent\npub fn added_fn() -> i32 {\n    42\n}\n";
                if let Some(parent) = std::path::Path::new(&write_file).parent() {
                    let _ = std::fs::create_dir_all(parent);
                }
                let _ = std::fs::write(&write_file, content);
            }

            if let Some(modify_file) = try_get_env("LOOPER_E2E_FAKE_AGENT_MODIFY_FILE") {
                let mut f = std::fs::OpenOptions::new()
                    .append(true)
                    .create(true)
                    .open(&modify_file)
                    .expect("cannot open modify file");
                let _ = writeln!(f, "// fake-agent modified this file");
            }

            let marker = std::env::var("LOOPER_COMPLETION_MARKER").unwrap_or_else(|_| DEFAULT_MARKER.to_string());

            println!("{}fake-agent: mode={} status=success summary=\"completed successfully\"", marker, mode);
            process::exit(0);
        }
        _ => {
            let marker = std::env::var("LOOPER_COMPLETION_MARKER").unwrap_or_else(|_| DEFAULT_MARKER.to_string());
            println!("{}fake-agent: mode={} status=success summary=\"completed successfully\"", marker, mode);
            process::exit(0);
        }
    }
}

fn write_evidence(artifact_dir: &str, mode: &str) {
    let evidence = serde_json::json!({
        "cwd": std::env::current_dir().map(|p| p.to_string_lossy().to_string()).unwrap_or_default(),
        "args": std::env::args().collect::<Vec<_>>(),
        "env": {
            "mode": mode,
            "prompt": std::env::var("LOOPER_PROMPT").unwrap_or_default(),
            "completion_marker": std::env::var("LOOPER_COMPLETION_MARKER").unwrap_or_default(),
            "write_file": std::env::var("LOOPER_E2E_FAKE_AGENT_WRITE_FILE").unwrap_or_default(),
            "modify_file": std::env::var("LOOPER_E2E_FAKE_AGENT_MODIFY_FILE").unwrap_or_default(),
            "sleep_ms": std::env::var("LOOPER_E2E_FAKE_AGENT_SLEEP_MS").unwrap_or_default(),
        },
        "timestamp": chrono::Utc::now().to_rfc3339(),
        "mode": mode,
        "pid": std::process::id(),
    });

    let evidence_path = std::path::Path::new(artifact_dir).join("cwd-evidence.json");
    if let Ok(content) = serde_json::to_string_pretty(&evidence) {
        let _ = std::fs::write(&evidence_path, &content);
    }
}

fn try_get_env(key: &str) -> Option<String> {
    let val = std::env::var(key).ok()?;
    if val.is_empty() {
        return None;
    }
    Some(val)
}
